// Cate list mobile
var Nav =document.querySelectorAll(".navigation-cate")
Nav[0].onclick  =() =>{
  var x= document.querySelectorAll(".list-cate")
  var y= x[0].getAttribute("class").lastIndexOf("list-cate-active")
  
    if(y===-1){
      x[0].classList.add("list-cate-active") 
    }
    else{
      x[0].classList.remove("list-cate-active")
    }
}
